The number of health care workers who are vaccinated every year is surprisingly low. A study reported in the <i>BMJ (British Medical Journal)</i>, in August 2009, found that approximately <a href="/articles/161678.php">half of all Hong Kong health care staff said they would not take the swine flu jab</a> because of fears regarding its effectiveness and potential side effects. This is unfortunate, the authors wrote, because "vaccination is one of the most effective ways to reduce illness and death linked with pandemic flu".<br><br>

