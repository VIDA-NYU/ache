<div itemprop="articleBody"> <b>Influenza, or flu, is a respiratory illness that is caused by a virus. Flu is highly contagious and is usually spread by the coughs and sneezes of a person who is infected.</b><br><br>

You can also catch flu from an infected person if you touch them (e.g. shaking hands). Adults are contagious one day before getting symptoms and up to 7 days after becoming ill. <a href="http://www.medicalnewstoday.com/articles/249792.php">This means that you can spread the influenza virus before you even know you are infected</a>. A flu epidemic, when a large number of people in one country are infected with flu, can last several weeks.<br><br>

According to the <a href="http://nccam.nih.gov/health/flu" target="_blank">National Institutes of Health</a>, between 5% and 20% of Americans get the flu each year. More than 200,000 people are hospitalized and 36,000 die annually because of flu in the United States.

