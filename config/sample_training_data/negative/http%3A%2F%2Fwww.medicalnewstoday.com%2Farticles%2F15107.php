<h2>What should I do if I have flu?</h2>

As flu is caused by a virus, <a href="/articles/10278.php" title="What are antibiotics? How do antibiotics work?" class="keywords">antibiotics</a> won't do any good, unless the flu has led to another illness. Some of the symptoms, such as headache and body pains may be alleviated if you take a painkiller. Some painkillers, such as <a href="/articles/161255.php" title="What is aspirin? What is aspirin for?" class="keywords">aspirin</a>, should not be given to children under 12 <i>(Department of Health, United Kingdom)</i>. If you have flu you should:

