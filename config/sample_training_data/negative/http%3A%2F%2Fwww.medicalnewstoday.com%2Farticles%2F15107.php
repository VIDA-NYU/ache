<h2>There are three types of flu viruses</h2>

Three types of flu viruses exist - <b>infuenza A, influenza B and influenza C</b>.  Types A and B viruses cause seasonal epidemics which hit the USA and Europe virtually every winter. The type C influenza virus causes mild respiratory illness and is not responsible for epidemics. <br><br>

Two proteins on the surface of influenza A viruses divide it into subtypes - the hemagglutinin (H) and the neuraminidase (N). 16 different hemagglutinin subtypes and 9 different neuraminidase subtypes are known to exist. The current subtypes of influenza A viruses found in people are A (<a href="/articles/147720.php" title="What is swine flu? How is swine flu treated?" class="keywords">H1N1</a>) and A (H3N2). <br><br>
There are no B virus subtypes, but there are different influenza B virus strains. <br><br>


