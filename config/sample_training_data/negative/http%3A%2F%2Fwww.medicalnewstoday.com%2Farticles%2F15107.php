<b>Flu in the USA</b><br><br>

In the United States approximately 5% to 20% of the population gets flu, according to the Centers of Disease Control and Prevention (CDC).  Over 200,000 people are hospitalized from flu complications each year, and about 36,000 people are estimated to die as a result of flu. <br><br>

<b>Flu in the world</b><br><br>

According to the World Health Organization (WHO), in annual influenza epidemics 5% to 15% of the world's population become ill with upper respiratory tract infections. Hospitalization and deaths mainly occur in high-risk groups. It is estimated that between one quarter to one half of a million people die each year as a result of flu. In industrialized countries the majority of deaths as a result of flu occur among people over the age of 65 years.

<h2>How to prevent flu</h2>

Health experts and government agencies throughout the world say that the single best way to protect yourself from catching flu is to get vaccination every year. There are two types of vaccinations, the <i>flu shot</i> and the <i>nasal-spray flu vaccine</i>. The flu shot is administered with a needle, usually in the arm - it is approved for people older than six months, including healthy people and th every year. There are two types of vaccinations, the <i>flu shot</i> and the <i>nasal-spray flu vaccine</i>. The flu shot is administered with a needose with chronic medical conditions. The nasal-spray flu vaccine is a vaccine made with live, weakened flu viruses that do not make you ill. <br><br>

A flu vaccine will contain three influenza viruses - One A (H3N2) virus, one A (H1N2) virus, and one B virus. As viruses adapt and change, so do those contained within the vaccines - what is included in them is based on international surveillance and scientists' calculations about which virus types and strains will circulate in a given year. You are protected about two weeks after receiving the vaccination. <br><br>

Annual flu vaccinations should start in September or as soon as the vaccine is on hand and continue throughout the flu season, into January, and beyond. This is because the timing and duration of influenza seasons are never the same. Flu outbreaks usually peak at around January, but they can happen as early as October. <br><br>

